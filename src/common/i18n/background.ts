function flattenMessages(target: LocaleMessages, prefix = ''): Record<string, string> {
  const result: Record<string, string> = {};

  for (const [key, value] of Object.entries(target)) {
    const fullKey = prefix ? `${prefix}.${key}` : key;

    if (typeof value === 'string') {
      result[fullKey] = value;
    } else if (value !== null && typeof value === 'object') {
      Object.assign(result, flattenMessages(value as LocaleMessages, fullKey));
    }
  }

  return result;
}

// 预计算扁平化的消息
const flattenedMessages: Record<LocaleCode, Record<string, string>> = {} as Record<
  LocaleCode,
  Record<string, string>
>;
for (const locale of SUPPORTED_LOCALES) {
  flattenedMessages[locale] = flattenMessages(messages[locale]);
}

// 翻译函数 - 支持 {{paramName}} 和 {paramName} 两种格式
export function t(key: string, params?: MessageParams): string {
  const localeData = flattenedMessages[currentLocale];
  let message = localeData?.[key];

  if (!message) {
    // 如果翻译不存在，尝试从 DEFAULT_LOCALE 获取
    const defaultMessage = flattenedMessages[DEFAULT_LOCALE]?.[key];
    if (defaultMessage) {
      message = defaultMessage;
    } else {
      return key; // 返回key作为后备
    }
  }

  if (params) {
    // 先替换 {{paramName}} 格式
    let result = message;
    result = result.replace(/\{\{(\w+)\}\}/g, (_, paramName) => {
      const value = params[paramName];
      if (value !== undefined) {
        return String(value);
      }
      return _;
    });
    // 再替换 {paramName} 格式
    result = result.replace(/\{(\w+)\}/g, (_, paramName) => {
      const value = params[paramName];
      if (value !== undefined) {
        return String(value);
      }
      return _;
    });
    return result;
  }

  return message;
}

// 初始化函数 - 仅设置语言
export function initBackgroundI18n(locale?: string): void {
  if (locale && SUPPORTED_LOCALES.includes(locale as LocaleCode)) {
    currentLocale = locale as LocaleCode;
  }
}
